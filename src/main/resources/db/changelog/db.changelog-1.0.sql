CREATE TABLE Bank_card
(
    bank_card_id   INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Card_holder_id INTEGER REFERENCES Users (user_id),
    expired_at     DATE,
    status         VARCHAR,
    balance        NUMERIC(12, 5)
);
